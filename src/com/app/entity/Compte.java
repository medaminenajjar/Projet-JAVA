	public void setSold(String sold) {
		this.sold = sold;
	}




	public String getDevise() {
		return devise;
	}




	public void setDevise(String devise) {
		this.devise = devise;
	}




	public boolean isCompteBloque() {
		return compteBloque;
	}




	public void setCompteBloque(boolean compteBloque) {
		this.compteBloque = compteBloque;
	}




